function dev(i, p) {

    $(document).ready(function() {
        i.click(function() {
            i.toggle();
            p.toggle();
        });
        p.click(function() {
            i.toggle();
            p.toggle();
        });
    });
}

dev($("#south"), $("#imageon"));



